public class Start {
    public static void main(String[] args) {
        System.out.println("The program is starting");
        //int - numero inteiro
        //long - numero com mais espaço de memoria
        //float - aceita numero com virgula pequeno
        //double - aceita numero com virgula grande
        //boolean - verdadeiro ou falso
        //char - apenas UM caracter
        //string - mais de um caracter
        int amount = 47; //€
        float littleDecimalNumber = 4.5f;
        double bigDecimalNumber = 4.5;
        boolean myBooleanFalse = false;
        char myChar = 'M';
        String name = "Maria";

        System.out.println("Numero inteiro: "+amount);
        System.out.println("Numero decimal pequeno: "+littleDecimalNumber);
        System.out.println("Numero decimal grande: "+bigDecimalNumber);
        System.out.println("Bollean: "+ myBooleanFalse);
        System.out.println("Char: "+ myChar);
        System.out.println("String: "+ name);
        System.out.println("The program is ending");
    }
}
